package com.bhavani.constructors;

public class _3ZeroParameterisedConstructor {
	int iValue;
	float fValue;

	public _3ZeroParameterisedConstructor() {
		iValue = iValue;
		fValue = fValue;
	}

	public static void main(String[] args) {
		_3ZeroParameterisedConstructor ref = new _3ZeroParameterisedConstructor();
		System.out.println(ref.iValue);
		System.out.println(ref.fValue);
	}
}
