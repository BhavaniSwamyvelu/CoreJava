package com.bhavani.thiskeyword;

public class _2Thiskeyword {
	int iVal = 10;
	float fVal = 3.14f;

	public _2Thiskeyword(int i, float f) {
		i = i;
		f = f;
	}

	public static void main(String[] args) {
		
		_2Thiskeyword ref = new _2Thiskeyword(20, 3.00f);
		System.out.println(ref.iVal + "\n" + ref.fVal);

	}
}
