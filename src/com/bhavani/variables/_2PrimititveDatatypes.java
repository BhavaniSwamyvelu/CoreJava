package com.bhavani.variables;

public class _2PrimititveDatatypes {
	public static void main(String[] args) {
		byte bValue = 20;
		short sValue = 2;
		int iValue = 10;
		long lValue = 123456789L;
		long llValue = 23452178l;
		float fValue = 3.14f;
		double dValue = 3.19999999;
		char cValue = 'a';
		boolean boolValue = true;
		System.out.println(bValue + "\n" + sValue + "\n" + iValue + "\n" + lValue + "\n" + llValue + "\n" + fValue
				+ "\n" + dValue + "\n" + cValue + "\n" + boolValue);

	}
}
