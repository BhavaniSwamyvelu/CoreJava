package com.bhavani.operators;

public class ArithOper {
	public static void main(String[] args) {
		int aVal = 40;
		int bVal = 20;
		int addVal = aVal + bVal;
		int diffVal = aVal - bVal;
		int mulVal = aVal * bVal;
		int divVal = aVal / bVal;
		int modVal = aVal % bVal;
		System.out.println(addVal + "\n" + diffVal + "\n" + mulVal + "\n" + divVal + "\n" + modVal);
	}
}
