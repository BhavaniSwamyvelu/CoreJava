package com.bhavani.operators;

public class CompAssignment {
	public static void main(String... args) {
		int cVal = 100;
		int aVal = 50;

		aVal += cVal;
		System.out.println(aVal);
		aVal -= cVal;
		System.out.println(aVal);
		aVal *= cVal;
		System.out.println(aVal);
		aVal /= cVal;
		System.out.println(aVal);
		aVal %= cVal;
		System.out.println(aVal);

	}

}
