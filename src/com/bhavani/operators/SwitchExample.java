package com.bhavani.operators;

public class SwitchExample {
	public static void main(String[] args) {
		int aVal = 6;
		switch (aVal) {

		case 1:
			System.out.println("FIRST CASE IS EXECUTED");
			break;
		default:
			System.out.println("default case is executed");
			break;
		case 2:
			System.out.println("SECOND CASE IS EXECUTED");
			break;
		case 3:
			System.out.println("THIRD CASE IS EXECUTED");
			break;
		}
	}

}
