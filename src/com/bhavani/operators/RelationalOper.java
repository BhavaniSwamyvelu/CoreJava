package com.bhavani.operators;

public class RelationalOper {
	public static void main(String[] args) {
		int aVal = 10;
		int bVal = 5;
		System.out.println("aVal == bVal = " + (aVal == bVal));
		System.out.println("aVal != bVal = " + (aVal != bVal));
		System.out.println("aVal > bVal = " + (aVal > bVal));
		System.out.println("aVal < bVal = " + (aVal < bVal));
		System.out.println("bVal >= aVal = " + (bVal >= aVal));
		System.out.println("bVal <= aVal = " + (bVal <= aVal));
	}
}
